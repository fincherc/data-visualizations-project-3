CREATE TABLE us_accidents (
    ID VARCHAR(255) PRIMARY KEY,
    Source VARCHAR(255),
    Severity INT,
    Start_Time TIMESTAMP,
    End_Time TIMESTAMP,
    Start_Lat FLOAT,
    Start_Lng FLOAT,
    End_Lat FLOAT,
    End_Lng FLOAT,
    Distance FLOAT,
    Description TEXT,
    Street VARCHAR(255),
    City VARCHAR(255),
    County VARCHAR(255),
    State VARCHAR(255),
    Zipcode VARCHAR(10),
    Country VARCHAR(255),
    Timezone VARCHAR(255),
    Airport_Code VARCHAR(10),
    Weather_Timestamp TIMESTAMP,
    Temperature FLOAT,
    Wind_Chill FLOAT,
    Humidity FLOAT,
    Pressure FLOAT,
    Visibility FLOAT,
    Wind_Direction VARCHAR(50),
    Wind_Speed FLOAT,
    Precipitation FLOAT,
    Weather_Condition VARCHAR(255),
    Amenity BOOLEAN,
    Bump BOOLEAN,
    Crossing BOOLEAN,
    Give_Way BOOLEAN,
    Junction BOOLEAN,
    No_Exit BOOLEAN,
    Railway BOOLEAN,
    Roundabout BOOLEAN,
    Station BOOLEAN,
    Stop BOOLEAN,
    Traffic_Calming BOOLEAN,
    Traffic_Signal BOOLEAN,
    Turning_Loop BOOLEAN,
    Sunrise_Sunset VARCHAR(50),
    Civil_Twilight VARCHAR(50),
    Nautical_Twilight VARCHAR(50),
    Astronomical_Twilight VARCHAR(50)
);